from aiogram import Router
from aiogram.filters import Command
from aiogram.types import Message

help_router = Router()

HELP_TEXT = (
    "ℹ️ Помощь по боту\n\n"
    "Как пользоваться:\n"
    "• /start — регистрация пациента и доступ к меню.\n"
    "• /measure — ручной ввод замера (выберите тег и введите число).\n"
    "• Кнопки меню — графики, статистика и настройки пациента.\n\n"
    "Настройки пациента:\n"
    "• Укажите утреннее/вечернее время и время пика — они влияют на напоминания.\n"
    "• Значения можно менять в меню «Настройки пациента».\n\n"
    "Про чаты:\n"
    "• Для разных котов можно создать отдельные чаты.\n"
    "• В чат можно добавлять всех, кто ухаживает за котом, включая врача — "
    "все будут видеть замеры и статистику.\n\n"
    "Важно:\n"
    "• Бот и его уведомления не заменяют лечение.\n"
    "• Консультация с врачом обязательна — бот лишь помогает собирать и анализировать данные."
)


@help_router.message(Command("help"))
async def help_command(message: Message):
    await message.answer(HELP_TEXT)
